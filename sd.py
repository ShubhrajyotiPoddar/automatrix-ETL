# --- TECHNICAL IMPLEMENTATION ---
lines.append("## üíª Technical Implementation")
lines.append("")
lines.append("### 1. The \"Upsert\" Logic (PostgreSQL + Python)")
lines.append("One of the main challenges was handling data that changes retroactively (e.g., a PO quantity is revised). Simply appending data would create duplicates. I implemented a robust `INSERT ... ON CONFLICT DO UPDATE` strategy.")
lines.append("")

lines.append("```python")
lines.append("# snippet from src/etl_process.py")
lines.append('insert_sql = """') 
lines.append("INSERT INTO pur_order_daily_main (")
lines.append("    order_date, item_code, order_number, order_value, ")
lines.append("    bal_qty, rate, department, cost_project")
lines.append(") VALUES (%s, %s, %s, %s, %s, %s, %s, %s)")
lines.append("ON CONFLICT (order_date, item_code, order_number, department, cost_project)")
lines.append("DO UPDATE SET")
lines.append("    order_value = EXCLUDED.order_value,")
lines.append("    bal_qty = EXCLUDED.bal_qty,")
lines.append("    rate = EXCLUDED.rate;")
lines.append('"""') 
lines.append("```")

